package main

import "fmt"

func main() {

	// Declaração de valores com o operador curto de atribuição
	x := 42 // int
	y := "James Bond" // string
	z := true // bool

	// Imprime em uma única declaração
	fmt.Println(x, y, z)

	// Imprime em múltiplas declarações
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}